    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String nama;
        String username;
        
        String sqll = "select * from masyarakat where username=? AND password=? AND level=?"; 
        
        try {
            pst = conn.prepareStatement(sqll);
            pst.setString(1, txt_userma.getText());
            pst.setString(2, txt_pasma.getText());
            pst.setObject(3, cmb_hakma.getSelectedItem());
            
            rs = pst.executeQuery();
            if (rs.next()) {
                nama = rs.getString("nama");
                username = rs.getString("username");
               
                session.setU_namaDepan(nama);
                session.setU_username(username);
                if(cmb_hakma.getSelectedItem() == "masyarakat"){
                    menu2 msy = new menu2();
                    msy.setVisible(true);
                    msy.setExtendedState(JFrame.MAXIMIZED_BOTH);
                    this.setVisible(false);
                    this.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Username and Password are INCORRECT");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton4ActionPerformed

