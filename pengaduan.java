    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String sql = "SELECT\n" +
"       pengaduan.id_pengaduan AS pengaduan_id_pengaduan, \n" +
"       pengaduan.nik AS pengaduan_nik, \n" +
"       pengaduan.tgl_pengaduan AS pengaduan_tgl_pengaduan, \n" +
"       pengaduan.isi_laporan AS pengaduan_isi_laporan, \n" +
"       pengaduan.status AS pengaduan_status, \n" +
"       masyarakat.nik AS masyarakat_nik, \n" +
"       masyarakat.nama AS masyarakat_nama, \n" +
"       masyarakat.username AS masyarakat_username, \n" +
"       masyarakat.password AS masyarakat_password, \n" +
"       masyarakat.telp AS masyarakat_telp, \n" +
"       masyarakat.level AS masyarakat_level \n" +
"       FROM masyarakat INNER JOIN pengaduan " +
"       ON masyarakat.nik = pengaduan.nik";

    try {
        String file = "F:\\NetBeansProjects\\layanan\\src\\report\\koneksi1.jasper";
        java.sql.Connection conn = Koneksi.koneksi.KoneksiDB();

        if (conn == null) {
            JOptionPane.showMessageDialog(null, "Koneksi database null!");
            return;
        }

        java.io.File reportFile = new java.io.File(file);
        if (!reportFile.exists()) {
            JOptionPane.showMessageDialog(null, "File report tidak ditemukan di: " + reportFile.getAbsolutePath());
            return;
        }

        JasperPrint jp = JasperFillManager.fillReport(file, null, conn);
        JasperViewer.viewReport(jp, false);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error Laporan: " + e.getMessage());
        e.printStackTrace();
    }
    
    }//GEN-LAST:event_jButton4ActionPerformed

