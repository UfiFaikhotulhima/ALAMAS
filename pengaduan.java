    void getData() {
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            //membuat statemen pemanggilan data pada table pada surat_masuk dari database
            String sql = "select * from pengaduan";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
        
            //penelusuran baris pada tabel surat_masuk dari database
            while (rs.next()) {
                Object[] obj = new Object[5];
                obj[0] = rs.getString("id_pengaduan");
                obj[1] = rs.getString("nik");
                obj[2] = rs.getString("tgl_pengaduan");
                obj[3] = rs.getString("isi_laporan");
                obj[4] = rs.getString("status");
                model.addRow(obj);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    void pilihData() throws ParseException {
        int i = jTable1.getSelectedRow();
        txt_pengaduan.setText(model.getValueAt(i, 0).toString());
        txt_nik.setText(model.getValueAt(i, 1).toString());
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date date = sdf.parse(model.getValueAt(i, 2).toString());
        jd_tglpengaduan.setDate(date);
        txt_isi.setText(model.getValueAt(i, 3).toString());
        txt_status.setText(model.getValueAt(i, 4).toString());
    }
    
